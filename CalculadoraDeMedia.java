public class CalculadoraDeMedia {
    public static void main(String[] args) {
        // Notas fornecidas
        double nota1 = 8;
        double nota2 = 9;
        double nota3 = 7;
        double nota4 = 9;

        // Calcular a média
        double media = (nota1 + nota2 + nota3 + nota4) / 4;

        // Exibir a média
        System.out.println("A média das notas é: " + media);
    }
}
